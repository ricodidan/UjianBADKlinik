    private void txCariKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txCariKeyTyped
        // TODO add your handling code here:
        DefaultTableModel tabel = new DefaultTableModel();

        tabel.addColumn("No");
        tabel.addColumn("Nama");
        tabel.addColumn("Alamat");
        tabel.addColumn("NIK");
        tabel.addColumn("Tanggal lahir");
        tabel.addColumn("Jenis kelamin");

        try {
            Connection c = Koneksi.getKoneksi();
            String sql = "Select * from pasien where nama like '%" + txCari.getText() + "%'";
            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                Date date = simpleDateFormatDefault.parse(rs.getString(5));
                String dateModified = simpleDateFormat.format(date);
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    dateModified,
                    rs.getString(6),});
            }
            jTable1.setModel(tabel);
            loadData();
        } catch (Exception e) {
            System.out.println("Cari Data Error");
        } finally {
        }
    }//GEN-LAST:event_txCariKeyTyped

