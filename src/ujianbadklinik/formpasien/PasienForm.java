    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String nama = txNama.getText();
        String alamat = txAlamat.getText();
        String nik = txNik.getText();
        String tanggalLahir = txTanggalLahir.getText();
        String jenisKelamin = txJenisKelamin.getText();

        try {
            Connection c = Koneksi.getKoneksi();
            String sql = "INSERT INTO `pasien`(`nama`, `alamat`, `nik`, `tanggal_lahir`, `jenis_kelamin`) VALUES (?,?,?,?,?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, nama);
            p.setString(2, alamat);
            p.setString(3, nik);
            p.setString(4, tanggalLahir);
            p.setString(5, jenisKelamin);
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
        } catch (SQLException e) {
            System.out.println("Terjadi Kesalahan");
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan");
        } finally {
            loadData();
            autonumber();
            clear();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            return;
        }
        String nama = (String) model.getValueAt(i, 1);
        String alamat = txAlamat.getText();
        String nik = txNik.getText();
        String tanggalLahir = txTanggalLahir.getText();
        String jenisKelamin = txJenisKelamin.getText();

        try {
            Connection c = Koneksi.getKoneksi();
            String sql = "UPDATE `pasien` SET `alamat`=?,`nik`=?,`tanggal_lahir`=?,`jenis_kelamin`=? WHERE `nama` =?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, alamat);
            p.setString(2, nik);
            p.setString(3, tanggalLahir);
            p.setString(4, jenisKelamin);
            p.setString(5, nama);

            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Terubah");
            btnSimpan.setEnabled(true);
            clear();
        } catch (SQLException e) {
            System.out.println("Update Error");
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan");
        } finally {
            loadData();
            autonumber();
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            return;
        }

        String nama = (String) model.getValueAt(i, 1);

        int question = JOptionPane.showConfirmDialog(null, "Yakin " + nama + " akan dihapus ?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if (question == JOptionPane.OK_OPTION) {
            try {
                Connection c = Koneksi.getKoneksi();
                String sql = "DELETE FROM pasien WHERE nama = ?";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, nama);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Data Terhapus");
            } catch (SQLException e) {
                System.out.println("Terjadi Kesalahan");
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan");
            } finally {
                loadData();
                autonumber();
                clear();
            }
        }
        if (question == JOptionPane.CANCEL_OPTION) {
        }
    }//GEN-LAST:event_btnHapusActionPerformed

